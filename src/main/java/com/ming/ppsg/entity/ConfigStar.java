package com.ming.ppsg.entity;

/**
 * 星级配置
 */
public class ConfigStar {

    private Long id;

    private String starName;

    public ConfigStar() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getStarName() {
        return starName;
    }

    public void setStarName(String starName) {
        this.starName = starName;
    }
}
